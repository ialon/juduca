class BGCBSUser
{
	/**************************************************************************/
	
	function isSignIn()
	{
		$user=$this->getCurrentUserData();
		return((int)$user->id===0 ? false : true);
	}
	
	/**************************************************************************/
	
	function getCurrentUserData()
	{
		return(wp_get_current_user());
	}
	
	/**************************************************************************/
}

