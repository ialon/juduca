class BGCBSPerformance
{
	/**************************************************************************/
	
	static function start()
	{
		global $bgcbs_performance_timer;
		$bgcbs_performance_timer=microtime(true); 
	}
	
	/**************************************************************************/
	
	static function stop()
	{
		global $bgcbs_performance_timer;
		
		$time=microtime(true)-$bgcbs_performance_timer;
		
		echo sprintf(esc_html__('Total time: %s','bookingo'),$time).'<br>';
	}
	
	/**************************************************************************/
}

